/// One track sizing function pair per §11.2: `minmax()` splits, `<flex>`
/// and `fit-content()` take an `auto` minimum, plain values serve as both.
#[derive(Clone, Debug)]
pub(crate) struct TrackSize {
    min: TrackFn,
    max: TrackFn,
}

#[derive(Clone, Debug, PartialEq)]
pub(crate) enum TrackFn {
    Fixed(Len),
    Auto,
    MinContent,
    MaxContent,
    Fr(f32),
    FitContent(Len),
}

impl TrackFn {
    fn definite(&self, basis: Option<f32>) -> Option<f32> {
        match self {
            TrackFn::Fixed(l) => l.resolve(basis).map(|v| v.max(0.0)),
            _ => None,
        }
    }

    fn is_intrinsic(&self) -> bool {
        matches!(
            self,
            TrackFn::Auto | TrackFn::MinContent | TrackFn::MaxContent | TrackFn::FitContent(_)
        )
    }
}

/// One track mid-§11: sizing functions plus base size / growth limit.
#[derive(Clone, Debug)]
pub(crate) struct Track {
    pub size: TrackSize,
    pub base: f32,
    limit: f32,
    /// §11.5's "infinitely growable" flag (the Peter Salas case).
    inf_growable: bool,
    /// Generated by an auto-FIT repetition: collapses when no item spans it.
    collapsible: bool,
    pub collapsed: bool,
}

impl Track {
    fn new(size: TrackSize, collapsible: bool) -> Track {
        Track {
            size,
            base: 0.0,
            limit: f32::INFINITY,
            inf_growable: false,
            collapsible,
            collapsed: false,
        }
    }
}

/// Parse one track-size token into its §11.2 (min, max) pair.
fn parse_track_size(tok: &str, u: Units, vp: Vp) -> Option<TrackSize> {
    let t = tok.trim();
    let lower = t.to_ascii_lowercase();
    let simple = |s: &str| -> Option<TrackFn> {
        match s.trim().to_ascii_lowercase().as_str() {
            "auto" => Some(TrackFn::Auto),
            "min-content" => Some(TrackFn::MinContent),
            "max-content" => Some(TrackFn::MaxContent),
            other => {
                if let Some(fr) = other.strip_suffix("fr") {
                    return fr
                        .trim()
                        .parse::<f32>()
                        .ok()
                        .filter(|f| *f >= 0.0)
                        .map(TrackFn::Fr);
                }
                Len::parse(s, u, vp).map(TrackFn::Fixed)
            }
        }
    };
    if let Some(inner) = lower
        .starts_with("minmax(")
        .then(|| t.get(7..t.len().saturating_sub(1)))
        .flatten()
        .filter(|_| lower.ends_with(')'))
    {
        let args = split_args(inner);
        if args.len() != 2 {
            return None;
        }
        let min = match simple(args[0])? {
            // A <flex> minimum is invalid grammar; §11.2 fallback is auto.
            TrackFn::Fr(_) => return None,
            f => f,
        };
        let max = simple(args[1])?;
        return Some(TrackSize { min, max });
    }
    if let Some(inner) = lower
        .starts_with("fit-content(")
        .then(|| t.get(12..t.len().saturating_sub(1)))
        .flatten()
        .filter(|_| lower.ends_with(')'))
    {
        let l = Len::parse(inner, u, vp)?;
        return Some(TrackSize {
            min: TrackFn::Auto,
            max: TrackFn::FitContent(l),
        });
    }
    match simple(t)? {
        TrackFn::Fr(f) => Some(TrackSize {
            // §11.2: a <flex> track's min sizing function is auto.
            min: TrackFn::Auto,
            max: TrackFn::Fr(f),
        }),
        f => Some(TrackSize {
            min: f.clone(),
            max: f,
        }),
    }
}

/// A parsed template segment: a plain track, or one auto-repetition group
/// (whose count resolves against the available space, §7.2.3.2).
enum Seg {
    One(TrackSize),
    AutoRepeat { reps: Vec<TrackSize>, fit: bool },
}

/// Parse a `grid-template-columns`/`-rows` value into tracks, expanding
/// `repeat()`. `None` = no usable template (`none`, empty, or unparseable —
/// the whole declaration is dropped, matching the invalid-declaration rule).
fn parse_template(
    value: &str,
    u: Units,
    vp: Vp,
    avail: Option<f32>,
    gap: f32,
) -> Option<Vec<Track>> {
    let v = value.trim();
    if v.is_empty() || v.eq_ignore_ascii_case("none") {
        return None;
    }
    let mut segs: Vec<Seg> = Vec::new();
    for tok in split_track_tokens(v) {
        let lower = tok.to_ascii_lowercase();
        if lower.starts_with("repeat(") && lower.ends_with(')') {
            let inner = &tok[7..tok.len() - 1];
            let (count, list) = inner.split_once(',')?;
            let sizes: Vec<TrackSize> = split_track_tokens(list)
                .iter()
                .map(|t| parse_track_size(t, u, vp))
                .collect::<Option<_>>()?;
            if sizes.is_empty() {
                return None;
            }
            match count.trim().to_ascii_lowercase().as_str() {
                "auto-fill" => segs.push(Seg::AutoRepeat {
                    reps: sizes,
                    fit: false,
                }),
                "auto-fit" => segs.push(Seg::AutoRepeat {
                    reps: sizes,
                    fit: true,
                }),
                n => {
                    let n: usize = n.parse().ok().filter(|&n| n >= 1)?;
                    // Lid against hostile repeat counts (the grid itself is
                    // capped by the terminal's paintable area anyway).
                    for _ in 0..n.min(1000) {
                        segs.extend(sizes.iter().cloned().map(Seg::One));
                    }
                }
            }
        } else {
            segs.push(Seg::One(parse_track_size(&tok, u, vp)?));
        }
    }
    // §7.2.3.2: the auto-repetition count is the largest N that doesn't
    // overflow the definite available space, gaps included; each repeated
    // track counts as its definite max else definite min; ≥ 1 always.
    let mut out: Vec<Track> = Vec::new();
    let fixed_sum: f32 = segs
        .iter()
        .map(|s| match s {
            Seg::One(ts) => ts
                .max
                .definite(avail)
                .or_else(|| ts.min.definite(avail))
                .unwrap_or(0.0),
            Seg::AutoRepeat { .. } => 0.0,
        })
        .sum();
    let n_fixed = segs.iter().filter(|s| matches!(s, Seg::One(_))).count();
    for seg in segs {
        match seg {
            Seg::One(ts) => out.push(Track::new(ts, false)),
            Seg::AutoRepeat { reps, fit } => {
                let rep_size: f32 = reps
                    .iter()
                    .map(|ts| {
                        let max = ts.max.definite(avail);
                        let min = ts.min.definite(avail);
                        match (max, min) {
                            (Some(mx), Some(mn)) => mx.max(mn),
                            (Some(mx), None) => mx,
                            (None, Some(mn)) => mn,
                            (None, None) => 0.0,
                        }
                    })
                    .sum();
                let k = reps.len() as f32;
                let n = match avail {
                    Some(av) if rep_size > 0.0 => {
                        let budget = av - fixed_sum - gap * (n_fixed as f32 - 1.0).max(0.0);
                        let per = rep_size + gap * k;
                        // total(N) = N·rep + gap·(N·k − 1) fits budget+gap
                        (((budget + gap) / per).floor() as isize).max(1) as usize
                    }
                    _ => 1,
                };
                for _ in 0..n.min(1000) {
                    out.extend(reps.iter().cloned().map(|ts| Track::new(ts, fit)));
                }
            }
        }
    }
    (!out.is_empty()).then_some(out)
}

